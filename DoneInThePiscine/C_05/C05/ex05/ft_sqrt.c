#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (n < nb && i <= 46340)
	{
		n = i * i;
		if (n == nb)
			break ;
		else
			i++;
	}
	if (n > nb || nb <= 0 || i == 46341)
		return (0);
	return (i);
}

