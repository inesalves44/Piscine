#include <unistd.h>

void	ft1(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		ft1('-');
		nb = -nb;
	}
	if (nb < 10)
	{
		ft1(nb + '0');
	}
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}

int main()
{
	ft_putnbr(9);
}
